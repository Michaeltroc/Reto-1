    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        System.out.println("Username:"+username+"\nPassword:"+password);
        //Validacion campos Login
        if(username.isEmpty() || password.isEmpty()){
            JOptionPane.showMessageDialog(this, "Faltan campos por diligenciar",username, JOptionPane.WARNING_MESSAGE);
        }else{
           //Si la validacion llega a ser exitosa
            if(username.equals("MisionTIC") && password.equals("Ciclo2")){;
                JOptionPane.showMessageDialog(this, "Bienvienido al Sistema");
                //Crear una instancia del jFrame UserMenu
                UserMenu userMenu = new UserMenu();
                userMenu.setVisible(true);
                //cerrar ventana del login
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Error. Revisa los valores suministrados", username,JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

