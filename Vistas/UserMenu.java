    private void listarEmpleados(){
        String query = "SELECT * FROM empleados";
        try{
            connection = conexion.getConnection();
            st = connection.createStatement();
            rs = st.executeQuery(query);
            Object[] empleados = new Object[5];
            contenidoTablaEmpleados = (DefaultTableModel)tblEmpleados.getModel();
            while(rs.next()){
                empleados[0] = rs.getInt("idEmp");
                empleados[1] = rs.getString("nombreEmp")+" "+rs.getString("apellidos");
                empleados[2] = rs.getString("tipoDocmento");
                empleados[3] = rs.getString("documento");
                empleados[4] = rs.getInt("correo");
                
                System.out.println("idEmp: " + empleados[0] + ", nombre; " +empleados[1] + ", documento: "
                    +empleados[2] + " " + empleados[3] +", correo: "+empleados[4]);
                
                contenidoTablaEmpleados.addRow(empleados);
                tblEmpleados.setModel(contenidoTablaEmpleados);
            }
        }catch(SQLException e){
            System.out.println("No se pudo cargar la informacion de los empleados");
        }
    }
    
